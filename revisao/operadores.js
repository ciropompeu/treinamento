//incremento
let numero = 10;

//depois da variavel vai mostrar o numero e só depois vai aumentando
console.log('incremento depois da variavel = ' + numero++);

//antes da variavel vai mostrar o numero ja incrementado
console.log('incremento antes da variavel');
console.log(++numero);
console.log(++numero);
console.log(++numero);
console.log(++numero);

// decremento
console.log('decremento antes da variavel =' + --numero);
console.log(--numero);
console.log(--numero);



//comparacao simples ==
// comparacao rigorosa ====

// desigualdade simples !=
// desigualdade rigorosa !==

// a respostas destas comparacoes serao sempre boleanas (true ou false)

console.log('comparacao de maior' + 10 > 5);
console.log('comparacao de menor' + 10 < 10);
console.log('comparacao de maior ou igual' + 20 >= 10);
console.log('comparacao de menor ou igual' + 20 < 10);

console.log('comparacao de igualdade');
console.log('ola' == 'ola');
console.log('comparacao de igualdade rigida');
console.log(5 === '5');

console.log('comparacao de diferentes');
console.log('ola' != 'oi');
console.log('comparacao de diferente');
console.log(10 !== '10');

console.log('comparador logico e &&');
console.log(10 > 5 && 8 < 10 && 11 < 5);

console.log('comparador logico ou ||');
console.log(10 > 5 || 8 < 10 || 11 < 5);

console.log('comparador logico de negacao !');
console.log(!true);
console.log(!5 > 3);

